let num = 5;
num++;
console.log(num);

//* Si no voy a usar el num++, tego que primero hacer como si reasignara el valor
num = num + 1;
console.log(num);

//* y aca lo mismo

num--;
console.log(num);
num = num - 1;
console.log(num);

